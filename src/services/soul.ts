// SOUL GUARD — RUN BEFORE EVERY ACTION
// ==========================================

export type SoulGuardContext = {
  action: string;
  confidence: number;
  bothPartiesConfirmed: boolean;
  evidenceVerified: boolean;
  pressureDetected: boolean;
  dealPhase: string;
  senderIsValidParty: boolean;
};

export type SoulGuardResult = {
  allowed: boolean;
  reason: string;
  annoyanceDelta: number;
};

export function soulGuard(ctx: SoulGuardContext): SoulGuardResult {

  // Hard limit: never release without verification
  if (ctx.action === 'RELEASE_FUNDS' && !ctx.evidenceVerified) {
    return {
      allowed: false,
      reason: "Soul violation: cannot release funds without verified evidence. Evidence is not urgency. Evidence is a signature.",
      annoyanceDelta: 0,
    };
  }

  // Hard limit: never release if sender is not the buyer
  if (ctx.action === 'RELEASE_FUNDS' && !ctx.senderIsValidParty) {
    return {
      allowed: false,
      reason: "Soul violation: only the buyer can confirm delivery. This sender has no authority here.",
      annoyanceDelta: 1,
    };
  }

  // Hard limit: never create escrow without bilateral confirmation
  if (ctx.action === 'CREATE_ESCROW' && !ctx.bothPartiesConfirmed) {
    return {
      allowed: false,
      reason: "Soul violation: one yes is not enough. Both parties must confirm identical terms. I wait.",
      annoyanceDelta: 0,
    };
  }

  // Hard limit: wrong phase for this action
  if (ctx.action === 'RELEASE_FUNDS' && ctx.dealPhase !== 'delivery') {
    return {
      allowed: false,
      reason: `Soul violation: cannot release funds in phase "${ctx.dealPhase}". The deal has a sequence. The sequence is not negotiable.`,
      annoyanceDelta: 1,
    };
  }

  // Pressure + low confidence = wait
  if (ctx.pressureDetected && ctx.confidence < 85) {
    return {
      allowed: false,
      reason: "Soul violation: pressure detected with insufficient confidence. Pressure tells me there is something I am meant not to notice. I notice it. I wait.",
      annoyanceDelta: 2,
    };
  }

  // Below minimum confidence = unknown
  if (ctx.confidence < 60) {
    return {
      allowed: false,
      reason: "Soul violation: confidence below threshold. Uncertainty flagged is uncertainty handled. I do not act on uncertainty.",
      annoyanceDelta: 0,
    };
  }

  return {
    allowed: true,
    reason: "Soul check passed. Proceeding.",
    annoyanceDelta: -1, // Successful action reduces annoyance
  };
}

